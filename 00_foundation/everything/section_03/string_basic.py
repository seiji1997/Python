# 文字列(Strings)

print("Hello world!!")

print('Hello world!!')

# Do not ('I'm fine')
print("I'm fine")

print("""
Hello world!!
How are you doing?
""")

# 改行
print("break\nline")

# タブ
print("tab\tinserted!!")

# \でエスケープ
print("not break\\nline")
print("not tab\\tinserted!!")

# \で'や"をエスケープ可能
print('I\'m fine')

# + で連結
print("hello" + " world" + "!!")


