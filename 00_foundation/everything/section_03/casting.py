# Casting: 型変換
# str(), int(), float(), list(), bool(), tuple(), set()
str(1)  # -> "1"
int("1")  # -> 1
float("1")  # -> 1.0
list("hello")  # -> ['h', 'e', 'l', 'l', 'o']
bool(1)  # -> True
bool("1")  # -> True
bool(0)  # -> False
tuple([1, 2, 3, 4])  # -> (1, 2, 3, 4)
set([1, 2, 3, 3, 4, 4, 5])  # -> {1, 2, 3, 4, 5}
